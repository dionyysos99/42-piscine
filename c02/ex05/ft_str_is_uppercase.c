int	ft_str_is_uppercase(char *src)
{
	int	i;

	i = 0;
	while (src[i] != '\0')
	{
		if (! (src[i] >= 'A' && src[i] <= 'Z'))
			return (0);
		i++;
	}
	return (1);
}
