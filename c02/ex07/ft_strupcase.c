char	*ft_strupcase(char *src)
{
	int	i;

	i = 0;
	while (src[i] != '\0')
	{
		if (src[i] >= 'a' && src[i] <= 'z')
			src[i] = src[i] - 'a' + 'A' ;
		i++;
	}
	return (src);
}
